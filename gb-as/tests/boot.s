
.org 0x40, 0x00
vblank: ; Vertical blank interrupt address

; Program start point
.org 0x100, 0x00

; Boot code
_init: ; Go to the entry point
    nop
    jp _start

; Program info

; Start up graphic
.byte 0xCE, 0xED, 0x66, 0x66, 0xCC, 0x0D, 0x00, 0x0B
.byte 0x03, 0x73, 0x00, 0x83, 0x00, 0x0C, 0x00, 0x0D
.byte 0x00, 0x08, 0x11, 0x1F, 0x88, 0x89, 0x00, 0x0E
.byte 0xDC, 0xCC, 0x6E, 0xE6, 0xDD, 0xDD, 0xD9, 0x99
.byte 0xBB, 0xBB, 0x67, 0x63, 0x6E, 0x0E, 0xEC, 0xCC
.byte 0xDD, 0xDC, 0x99, 0x9F, 0xBB, 0xB9, 0x33, 0x3E

; Game title
.ascii "GAME-TITLE-HERE"
; An Original Game Boy title
.byte 0x00
; License code bytes
.byte 0x00, 0x00
; Not for Super Game Boy
.byte 0x00

; Cart type: ROM only
.byte 0x00
; ROM size: 32KB
.byte 0x00
; RAM size: 0
.byte 0x00

; Destination: JP
.byte 0x00
; License code
.byte 0x01
; Mask rom version
.byte 0x00

; Complement check
.byte 0xCC
; Checksum
.byte 0x25, 0x2D

_start: ; Jump to the main routine
    nop
    stop
    call main
    jp _start

main: ; Store registers on the stack
    push af
    push bc
    push de
    push hl

    ; Do some operation

    ; Restore registers from the stack
    pop hl
    pop de
    pop bc
    pop af
    ret

; End of program rom
.org 0x8000, 0x00
